def is_logged_in_user(profile: dict) -> bool:
    """로그인 사용자 여부 확인"""
    return bool(profile.get("user_id"))

def get_general_response_template(message: str, profile: dict = None) -> str:
    """일반 질문에 대한 템플릿 응답 (사용자 상태별)"""
    
