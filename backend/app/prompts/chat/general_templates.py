    keto_keywords = ["키토", "다이어트", "뭐야", "무엇", "설명", "알려줘"]
    if any(keyword in message_lower for keyword in keto_keywords):
        return KETO_EXPLANATION_TEMPLATE_LOGGED_IN if is_logged_in else KETO_EXPLANATION_TEMPLATE_GUEST
    
